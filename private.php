    <h1>Engagement de Confidentialité (Politique de Confidentialité)</h1>
    <p><strong>Dernière mise à jour :</strong> 5 avril 2026</p>

    <h2>1. Introduction</h2>
    <p>Bienvenue sur l'application <strong>CardEvent</strong> ("l'Application"). Nous accordons une grande importance à la protection de vos données personnelles et au respect de votre vie privée. Le présent Engagement de Confidentialité a pour but de vous informer sur la manière dont nous collectons, utilisons, stockons et protégeons vos informations lors de l'utilisation de notre Application iOS.</p>

    <h2>2. Collecte des données</h2>
    <p>Lors de votre utilisation de l'Application, nous pouvons être amenés à collecter les données personnelles suivantes :</p>
    <ul>
        <li><strong>Informations de compte :</strong> pseudo, adresse e-mail et mot de passe (crypté) utilisés pour vous identifier de manière unique.</li>
        <li><strong>Informations techniques :</strong> identifiant d'appareil (ex: token APNs pour l'envoi des notifications Push), type d'appareil, version du système d'exploitation, données de crash pour l'amélioration continue de nos services.</li>
        <li><strong>Données d'utilisation :</strong> statistiques de jeu, historiques d'activités sur l'Application, participations aux activités.</li>
    </ul>

    <h2>3. Utilisation des données</h2>
    <p>Vos données sont exclusivement utilisées pour :</p>
    <ul>
        <li>Gérer votre authentification et sécuriser l'accès à votre compte personnel.</li>
        <li>Vous permettre de participer aux événements (inscription, annulation de participation).</li>
        <li>Vous envoyer des notifications importantes relatives aux prochains événements ou modifications de planning (Push Notifications), si vous y avez consenti.</li>
        <li>Améliorer et optimiser le fonctionnement de l'Application.</li>
    </ul>

    <h2>4. Partage et divulgation des données</h2>
    <p>Nous ne vendons, n'échangeons, ni ne louons vos données personnelles à des tiers. Vos informations peuvent être partagées uniquement dans le cadre strict des dispositions légales si la loi l'exige.</p>

    <h2>5. Sécurité des données</h2>
    <p>L'Application CardEvent met en œuvre des mesures de sécurité techniques et organisationnelles appropriées pour protéger vos informations contre la perte, le vol, l'accès non autorisé, la divulgation ou la modification. La communication avec nos serveurs est sécurisée (chiffrement TLS/SSL).</p>

    <h2>6. Vos droits</h2>
    <p>Conformément à la réglementation en vigueur (et au Règlement Général sur la Protection des Données - RGPD), vous disposez d'un droit d'accès, de rectification, de limitation et de suppression de vos données personnelles. Vous pouvez exercer ces droits à tout moment en nous contactant.</p>

    <h2>7. Suppression de compte</h2>
    <p>Si vous souhaitez que vos données soient intégralement supprimées ou si vous voulez clôturer votre compte, vous pouvez en faire la demande via l'Application ou en nous contactant par e-mail. Vos données seront alors définitivement effacées de nos serveurs.</p>

    <h2>8. Contact</h2>
    <p>Pour toute question concernant cette Politique de Confidentialité ou la gestion de vos données, veuillez nous contacter via notre formulaire de contact : <a href="https://viendez.com/assist.php">https://viendez.com/assist.php</a>.</p>

